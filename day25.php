    function getCountOfLockAndKeys($input) {
        $lock_and_keys = parse($input);
        
        $count = 0;
        foreach ($lock_and_keys['locks'] as $lock) {
            foreach ($lock_and_keys['keys'] as $key) {
                $fit = true;
                for ($i =0; $i < 5; $i++) {
                    if ($lock[$i] + $key[$i] >= 6) {
                        $fit = false;
                    }
                }
                if ($fit) $count += 1;
            }
        }
        
        return $count;
    }

    function parse($input) {
        $keys = [];
        $locks = [];
        
        $input = explode("\n", $input);
        $y = 0;
        
        while ($y < count($input)) {
            $next = array_slice($input, $y, 8);
            $isLock = $next[0] == '#####';
            
            if ($isLock) {
                $lock = [];
                for($i =0; $i < 8; $i++) {
                    $chars = str_split($next[$i]);
                    for($j =0; $j < count($chars); $j++) {
                        if ($chars[$j] == '#') $lock[$j] = $i;
                    }
                }
    
                $locks[] = $lock;
            } else {
                $key = [];
                for($i =7; $i >= 0; $i--) {
                    $chars = str_split($next[$i]);
                    for($j =0; $j < count($chars); $j++) {
                        if ($chars[$j] == '#') $key[$j] = 6 - $i;
                    }
                }
                
                $keys[] = $key;
            }
            
            
            $y+=8;
    
        }
        
        return [
            "locks" => $locks,
            "keys" => $keys
        ];
        
    }
        
        
        
