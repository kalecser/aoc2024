function countCheatsUpUntil($start_node, $node, $grid, $iterations_total, $iterations_left, $up_until_saving, &$start_end_destinantions_visited, &$start_end_destinantions_computed) {
	if ($iterations_left == 0) return 0;
	
	$possible_cheats = [[1,0],[-1,0],[0,1],[0,-1]];
	$result = 0;
	foreach ($possible_cheats as $c) {
		$teleported_to_node = $grid[$node->position_array_xy[1] + $c[0]][$node->position_array_xy[0] + $c[1]] ??null;
		
		if ($teleported_to_node == null) {
			continue;
		}
		
		$inner_visit_key = $start_node->position_array_xy[0] . ':' . $start_node->position_array_xy[1] . $node->position_array_xy[0] . $node->position_array_xy[1] . ':' .$iterations_left . ':' .  $teleported_to_node->position_array_xy[0] . ':' . $teleported_to_node->position_array_xy[1];
		if ($start_end_destinantions_visited[$inner_visit_key] ?? false) {
			continue;
		}
		
		$start_end_destinantions_visited[$inner_visit_key] = true;
		
		if (($teleported_to_node->type == '.' || $teleported_to_node->type == 'E') && $teleported_to_node->distance > $start_node->distance){
			$s = ($start_node->distance - $teleported_to_node->distance) + (($iterations_total - $iterations_left) + 1);
			
			
			if ($s <= $up_until_saving) {
				if ($start_end_destinantions_computed[var_export($start_node->position_array_xy, true) . var_export($teleported_to_node->position_array_xy, true)] ??true) {
					$start_end_destinantions_computed[var_export($start_node->position_array_xy, true) . var_export($teleported_to_node->position_array_xy, true)] = false;
					$result += 1;
				}
			}
		}
		
		$result += countCheatsUpUntil($start_node, $teleported_to_node, $grid, $iterations_total, $iterations_left - 1, $up_until_saving, $start_end_destinantions_visited, $start_end_destinantions_computed);
		
	}
	
	return $result;
}

function parseMaze(string $mazeString): array {
	$lines = explode("\n", trim($mazeString));
	$charArray = array_map('str_split', $lines);
	return $charArray;
}
	
function internalCountSteps(&$grid) {
	$nodes = [];
	
	$start = null;
	$end = null;
	
	//populate grid
	for ($i =0; $i < count($grid); $i++) {
		for ($h =0; $h < count($grid); $h++) {
			$type = $grid[$i][$h] ?? ' ';
			$n = new Node($type, [$h, $i]);
			if (($type) == 'S') {
				$start = $n;
			}
			if (($type) == 'E') {
				$end = $n;
			}
			$grid[$i][$h] = $n;
		}
	}
	
	//connect grid
	for ($i =0; $i < count($grid); $i++) {
		for($h = 0; $h < count($grid[0]); $h++) {
			$grid[$i][$h]->top = $grid[$i - 1][$h] ?? null;
			$grid[$i][$h]->left = $grid[$i][$h - 1] ?? null;
			$grid[$i][$h]->right = $grid[$i][$h + 1] ?? null;
			$grid[$i][$h]->bottom = $grid[$i + 1][$h] ?? null;
		}
	}
	
	//initialize queue with start node
	$queue = new ReversePriorityQueue();
	$start->distance = 0;
	$queue->insert($start, 0);
	
	$bearings = ["top", "left", "right", "bottom"];
	while (!$queue->isEmpty()) {
		
		$n = $queue->extract();
		
		if ($n->type == '#') { // wall
			continue;
		}
		
		foreach ($bearings as $bearing) {
			if ($n->visited) continue;
			if ($n->$bearing == null) continue;
			
			if ($n->$bearing->type == '#') { // wall
				continue;
			}
			
			if ($n->$bearing->visited) { //already visited
				continue;
			}
			
			$weightBearing = 'weight' . $bearing;
			$new_distance = ($n->distance + 1);
			if ($n->$bearing->distance >= $new_distance){
				if ($n->$bearing->distance > $new_distance) {
					$n->$bearing->ancestors = [];
					$n->$bearing->distance = $new_distance;
				}
				
				$n->$bearing->ancestors[] = $n->position_array_xy; //keep backtrack
				$queue->insert($n->$bearing, $n->$bearing->distance);
			}
		}
		
		$n->visited = true;
	}
	
	return ($end->distance);
}
	
function printGrid($grid) {
	for ($i = 1; $i < count($grid); $i++) {
		for ($j = 0; $j < count($grid); $j++) {
			if ($grid[$i][$j]->type == '#') echo '###';
			if ($grid[$i][$j]->type == 'S') echo 'SSS';
			if ($grid[$i][$j]->type == 'E') echo 'EEE';
			if ($grid[$i][$j]->distance > 999) $grid[$i][$j]->distance = 999;
			if ($grid[$i][$j]->type == '.') echo str_pad('' . $grid[$i][$j]->distance, 3, '0', STR_PAD_LEFT);
		}
		echo "\n";
	}
}
	
class Node {
	
	public $type;
	public $top;
	public $bottom;
	public $left;
	public $right;
	public $position_array_xy;
	public $visited = false;
	public $listed = false;
	public $ancestors = [];
	public $is_main_path = false;
	
	public $distance = PHP_INT_MAX;
	
	public function __construct($type, $position){
		$this->type = $type;
		$this->position_array_xy = $position;
	}
	
}
	
class ReversePriorityQueue extends SplPriorityQueue {
	public function compare($priority1, $priority2): int {
		// Reverse the comparison logic
		return $priority2 <=> $priority1; // Lowest priority first
	}
}
	
