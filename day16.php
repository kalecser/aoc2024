<?php
	

$lines = explode("\n", sample());
$grid = array_map('str_split', $lines);

$bearings = ['top', 'left', 'right', 'bottom'];

$nodes = [];

//populate grid
for ($i =0; $i < count($grid); $i++) {
	for($h = 0; $h < count($grid[0]); $h++) {
		$type = $grid[$i][$h];
		$grid[$i][$h] = [];
		
		$nodes[]= $grid[$i][$h]['top'] = new Node($type, [$h, $i], 'top');
		$nodes[]= $grid[$i][$h]['left'] = new Node($type, [$h, $i], 'left');
		$nodes[]= $grid[$i][$h]['right'] = new Node($type, [$h, $i], 'right');
		$nodes[]= $grid[$i][$h]['bottom'] = new Node($type, [$h, $i], 'bottom');
	}
}

//connect grid
for ($i =0; $i < count($grid); $i++) {
	for($h = 0; $h < count($grid[0]); $h++) {
		foreach ($bearings as $b) {
			$grid[$i][$h][$b]->top = $grid[$i - 1][$h]['top'] ?? null;
			$grid[$i][$h][$b]->left = $grid[$i][$h - 1]['left'] ?? null;
			$grid[$i][$h][$b]->right = $grid[$i][$h + 1]['right'] ?? null;
			$grid[$i][$h][$b]->bottom = $grid[$i + 1][$h]['bottom'] ?? null;
		}
	}
}
	
class ReversePriorityQueue extends SplPriorityQueue {
	public function compare($priority1, $priority2): int {
		// Reverse the comparison logic
		return $priority2 <=> $priority1; // Lowest priority first
	}
}

//initialize queue with start node
$queue = new ReversePriorityQueue();
$start = null;
foreach ($nodes as $n) {
	if ($n->type == 'S') {
		$n->distance = 0;
		if ($n->bearing == 'right') {
			$start = $n;
		}
	}
}	
$queue->insert($start, 0);


while (!$queue->isEmpty()) {

	$n = $queue->extract();

	if ($n->type == '#') { // wall
		continue;
	}
	
	foreach ($bearings as $bearing) {
		if ($n->$bearing->type == '#') { // wall
			continue;
		}
		
		if ($n->$bearing->visited) { //already visited
			continue;
		}
		
		$weightBearing = 'weight' . $bearing;
		$new_distance = ($n->distance + $n->$weightBearing);
		if ($n->$bearing->distance >= $new_distance){
			if ($n->$bearing->distance > $new_distance) {
				$n->$bearing->ancestors = [];
				$n->$bearing->distance = $new_distance;
				$n->$bearing->ancestors[] = [$n->position_array_xy, $n->bearing];
			}
			
			$n->$bearing->ancestors[] = [$n->position_array_xy, $n->bearing]; //keep backtrack
			$queue->insert($n->$bearing, $n->$bearing->distance);
		}
	}
	
	$n->visited = true;
}

$min_dist = PHP_INT_MAX;
foreach ($nodes as $n) {
	if ($n->type == 'E') {
		$min_dist = min($n->distance, $min_dist);
	}
}


$backtrack = [];
$stack = [];
foreach ($nodes as $n) {
	if ($n->type == 'E' && $n->distance == $min_dist) {
		array_push($stack, [$n->position_array_xy, $n->bearing]);
	}
}

//unique ancestors
while(count($stack) > 0) {
	$e = array_pop($stack);
	$key = $e[0][0] . ':' . $e[0][1];
	$n = $grid[$e[0][1]][$e[0][0]][$e[1]] ?? null;
	if ($n->listed) continue;
	$backtrack[$key] = true;
	foreach ($n->ancestors as $b) {
		array_push($stack, $b);
	}
	$n->listed = true;
}
	
echo("phase1 expected: 160624 actual: " . $min_dist . "\n");
echo("phase2 expected: 692 actual: " . count(array_keys($backtrack)) . "\n");

class Node {
	
	public $type;
	public $top;
	public $bottom;
	public $left;
	public $right;
	public $weighttop = 1001;
	public $weightleft = 1001;
	public $weightright = 1001;
	public $weightbottom = 1001;
	public $position_array_xy;
	public $bearing;
	public $visited = false;
	public $listed = false;
	public $ancestors = [];
	
	public $distance = PHP_INT_MAX;
	
	public function __construct($type, $position, $facing){
		$this->type = $type;
		$this->position_array_xy = $position;
		
		$this->bearing = $facing;
		
		if ($facing == 'top') {
			$this->weighttop = 1;
		} else if ($facing == 'left') {
			$this->weightleft = 1;
		} else if ($facing == 'right') {
			$this->weightright = 1;
		} else if ($facing == 'bottom') {
			$this->weightbottom = 1;
		}
	}
	
}

	
